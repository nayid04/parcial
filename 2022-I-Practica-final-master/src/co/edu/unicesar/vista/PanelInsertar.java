    private void boxTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boxTipoActionPerformed
        if (boxTipo.getSelectedItem().equals("Seleccionar Tipo")) {
            util.deshabilitarPanel(libro);
            util.deshabilitarPanel(audioLibro);
            //--------------------
            textEdicion.setText("Ingrese la edicion...");
            textEdicion.setForeground(new Color(204, 204, 204));
            //---------------------
            textPag.setText("Ingrese el No PAG...");
            textPag.setForeground(new Color(204, 204, 204));
            //---------------------
            textPeso.setText("Ingrese el peso...");
            textPeso.setForeground(new Color(204, 204, 204));
            //---------------------
            textFormato.setText("Ingrese el formato...");
            textFormato.setForeground(new Color(204, 204, 204));
            //---------------------
            textDuracion.setText("Ingrese la duracion...");
            textDuracion.setForeground(new Color(204, 204, 204));
        }
        if (boxTipo.getSelectedItem().equals("Libro")) {
            //mostrar panel libro
            audioLibro.setVisible(false);
            libro.setVisible(true);

            //habilitar panel libro
            util.habilitarPanel(libro);
        }
        
        if (boxTipo.getSelectedItem().equals("Audio Libro")) {
            //mostrar el panel audio libro
            libro.setVisible(false);
            audioLibro.setVisible(true);

            //habilitar el panel del libro
            util.habilitarPanel(audioLibro);
        }
    }//GEN-LAST:event_boxTipoActionPerformed

