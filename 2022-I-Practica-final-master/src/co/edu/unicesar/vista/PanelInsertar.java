    private void botonRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonRegistrarMouseClicked
        boolean vacio = true;
        if (textIsbn.getText().equals("Ingrese el isbn...") || textTitulo.getText().equals("Ingrese el titulo...") || textAutor.getText().equals("Ingrese el autor...") || textAnio.getText().equals("Ingrese el año pub...") || textAnio.getText().equals("Ingrese el año pub...") || textCosto.getText().equals("Ingrese el precio...") || boxTipo.getSelectedItem().equals("Seleccionar Tipo")) {
            JOptionPane.showMessageDialog(null, "complete todas las casillas de informacion.");

        }
        if (boxTipo.getSelectedItem().equals("Libro")) {
            if (textPag.getText().equals("Ingrese el No PAG...") || textEdicion.getText().equals("Ingrese la edicion...")) {
                JOptionPane.showMessageDialog(null, "complete la informacion del libro");
                vacio = true;
            } else {
                vacio = false;
            }
        }
        if (boxTipo.getSelectedItem().equals("Audio Libro")) {
            if (textFormato.getText().equals("Ingrese el formato...") || textDuracion.getText().equals("Ingrese la duracion...") || textPeso.getText().equals("Ingrese el peso...")) {
                JOptionPane.showMessageDialog(null, "complete la informacion del audio libro");
                vacio = true;
            } else {
                vacio = false;
            }
        }
        if (vacio == false) {

            leerNuevaPublicacion();
            registrarPublicacion();
            archivo.textPublicaciones(modelo);

            JOptionPane.showMessageDialog(null, "Datos guardados");

            limpiar();
        }
    }//GEN-LAST:event_botonRegistrarMouseClicked

